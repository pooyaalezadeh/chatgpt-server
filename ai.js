function pick(arr){

    let filtered = arr.filter(x => !lastReplies.includes(x));

    if(filtered.length === 0){
        lastReplies = [];
        filtered = arr;
    }

    let chosen = filtered[Math.floor(Math.random() * filtered.length)];

    lastReplies.push(chosen);

    if(lastReplies.length > 10){
        lastReplies.shift();
    }

    return chosen;
}
